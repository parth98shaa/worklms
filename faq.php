                <h3> How is yoga different from stretching, or other kinds of fitness?</h3>
                <div class="faq-content">
                  <p>The practice of yoga is more than just physically moving your body.  Within the physical practice you connect movements to the breath, which helps to focus and relax your mind.  In a yoga class, you are asked to bring your awareness to the present moment - to focus on the here and now - using your body and breath as an anchor.  This enhances your self-awareness so in time you start to notice more about how your body moves and what feels good or not so good.  Yoga also asks you to be OK with what is, without judgment. Just because you aren’t as flexible as someone else, or not as flexible as you used to be, doesn’t mean you aren’t “good at yoga”.  Over time you take these learnings off the mat and start to practice these concepts in your life as well.  As the postures improve your strength and flexibility, yoga also improves your mental health and overall well-being. </p>

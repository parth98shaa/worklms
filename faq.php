                <h3>Can I practice yoga while menstruating?</h3>
                <div class="faq-content">
                  <p>I hate to say it again, but it depends.  Some women prefer to pause their practice while menstruating while others continue practicing. I would suggest that you slow down your practice during your heavy flow days.  On those days you only have so much energy to give and should not deplete yourself from practicing yoga.  There is a lot of research out there on this specific topic, but again, my answer is to do what feels best for you.     </p>

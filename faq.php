                <h3>How do I know if I’m doing it right? </h3>
                <div class="faq-content">
                  <p>Before I dive into this one, it’s important to say that everyone’s bodies and bone structure are all very different, so there isn’t just one “right” way to do a pose. It’s about finding out what works for your own body.  Developing this body awareness takes practice. As long as something doesn’t hurt, you are probably ok.  You’ll definitely experience different sensations and feel your muscles working, but if you feel any type of “warning” pain, such as sharpness or pinching, make sure to listen to your body and come out of the pose slowly.</p>

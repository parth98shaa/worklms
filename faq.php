                <h3>Should I wait to eat before practicing the poses?  Or should I wait to eat after?</h3>
                <div class="faq-content">
                  <p>In general, I would say eat at least 2 hours before practicing the poses.  Of course it’s fine to eat something small if you need the nourishment to get through a class.  Just don’t practice the poses on a full stomach.  After practice, I would suggest waiting 15-30 minutes to eat.  This just gives your body time to move away from the physical practice and get ready to digest.  </p>
